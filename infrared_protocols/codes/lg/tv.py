"""Command codes for LG TVs."""

from enum import Enum, IntEnum

from ...commands import Command, NECCommand


class LGTVCode(IntEnum):
    """LG TV IR command codes."""

    ASPECT = 0x79
    BACK = 0x28
    BLUE = 0x72
    CHANNEL_DOWN = 0x01
    CHANNEL_UP = 0x00
    EXIT = 0x5B
    FAST_FORWARD = 0x8E
    GREEN = 0x63
    GUIDE = 0xA9
    HDMI_1 = 0xCE
    HDMI_2 = 0xCC
    HDMI_3 = 0xE9
    HDMI_4 = 0xDA
    HOME = 0x7C
    INFO = 0xAA
    INPUT = 0x0B
    LIST = 0xCA
    MENU = 0x43
    MUTE = 0x09
    NAV_DOWN = 0x41
    NAV_LEFT = 0x07
    NAV_RIGHT = 0x06
    NAV_UP = 0x40
    NUM_0 = 0x10
    NUM_1 = 0x11
    NUM_2 = 0x12
